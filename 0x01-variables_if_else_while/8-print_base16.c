#include <stdio.h>

/**
 * main - Entry point
 * Description: 'decide if a number is positive or negative'
 * Return: Always 0 (Success)
 */


int main(void)
{
	int ch;

	for (ch = 48 ; ch <= 57 ; ch++)
		putchar((char)ch);
	for (ch = 97 ; ch <= 102 ; ch++)
		putchar((char)ch);
	putchar('\n');
	return (0);
}
